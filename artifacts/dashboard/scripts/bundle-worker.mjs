#!/usr/bin/env node
// Bundles _worker.js + static assets into a single CF Worker script
// Output: dist/combined-worker.mjs

import { readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join, basename } from 'path';
import { fileURLToPath } from 'url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const DIST = join(__dirname, '../dist/public');
const OUT = join(__dirname, '../dist/combined-worker.mjs');

function getMime(f) {
  if (f.endsWith('.js')) return 'application/javascript';
  if (f.endsWith('.css')) return 'text/css';
  if (f.endsWith('.html')) return 'text/html';
  if (f.endsWith('.svg')) return 'image/svg+xml';
  if (f.endsWith('.jpg') || f.endsWith('.jpeg')) return 'image/jpeg';
  if (f.endsWith('.png')) return 'image/png';
  if (f.endsWith('.ico')) return 'image/x-icon';
  if (f.endsWith('.txt')) return 'text/plain';
  if (f.endsWith('.json')) return 'application/json';
  return 'application/octet-stream';
}

const SKIP_FILES = ['MR_ROBOT.apk', '_headers', '_worker.js'];
const MAX_FILE_BYTES = 2 * 1024 * 1024; // 2MB per file cap

const assets = {};
function walk(dir, rel) {
  for (const f of readdirSync(dir)) {
    const abs = join(dir, f);
    const relPath = (rel ? rel + '/' : '') + f;
    if (SKIP_FILES.includes(f)) continue;
    const stat = statSync(abs);
    if (stat.isDirectory()) {
      walk(abs, relPath);
    } else {
      if (stat.size > MAX_FILE_BYTES) {
        console.warn(`Skipping large file: ${relPath} (${Math.round(stat.size/1024)}KB)`);
        continue;
      }
      const content = readFileSync(abs);
      const mime = getMime(f);
      const isBinary = mime.startsWith('image/') && !mime.includes('svg');
      assets['/' + relPath] = {
        mime,
        data: isBinary ? content.toString('base64') : content.toString('utf8'),
        binary: isBinary,
      };
    }
  }
}
walk(DIST, '');

console.log('Embedded assets:');
Object.entries(assets).forEach(([k, v]) =>
  console.log(` ${k}: ${Math.round((v.binary ? v.data.length * 0.75 : Buffer.byteLength(v.data, 'utf8')) / 1024)}KB`)
);

// Read original _worker.js and patch env.ASSETS.fetch → serveStaticOrSPA
let workerCode = readFileSync(join(DIST, '_worker.js'), 'utf8');
const PATCH_TARGET = 'return env.ASSETS.fetch(request);';
if (!workerCode.includes(PATCH_TARGET)) {
  console.error('ERROR: Could not find patch target in _worker.js:', PATCH_TARGET);
  process.exit(1);
}
// Redirect / to /mr-professor, then serve static/SPA for everything else
workerCode = workerCode.replace(
  PATCH_TARGET,
  'if (url.pathname === "/" || url.pathname === "") { return Response.redirect(new URL("/mr-professor", request.url).toString(), 302); } return serveStaticOrSPA(url.pathname);'
);

const preamble = [
  '// Static assets embedded at build time — generated by bundle-worker.mjs',
  'const __STATIC = ' + JSON.stringify(assets) + ';',
  '',
  'function getCacheHeader(p) {',
  '  if (p.startsWith("/assets/")) return "public, max-age=31536000, immutable";',
  '  return "public, max-age=86400";',
  '}',
  '',
  'function serveStaticOrSPA(urlPath) {',
  '  const asset = __STATIC[urlPath] || __STATIC[urlPath + "/index.html"];',
  '  if (asset) {',
  '    let body;',
  '    if (asset.binary) {',
  '      const bstr = atob(asset.data);',
  '      const bytes = new Uint8Array(bstr.length);',
  '      for (let i = 0; i < bstr.length; i++) bytes[i] = bstr.charCodeAt(i);',
  '      body = bytes;',
  '    } else {',
  '      body = asset.data;',
  '    }',
  '    const ct = asset.mime + (asset.mime.startsWith("text/") || asset.mime === "application/javascript" ? "; charset=utf-8" : "");',
  '    return new Response(body, {',
  '      headers: { "Content-Type": ct, "Cache-Control": getCacheHeader(urlPath) }',
  '    });',
  '  }',
  '  const html = __STATIC["/index.html"];',
  '  if (html) return new Response(html.data, { headers: { "Content-Type": "text/html; charset=utf-8", "Cache-Control": "no-cache" } });',
  '  return new Response("Not Found", { status: 404 });',
  '}',
  '',
].join('\n');

const combined = preamble + workerCode;
writeFileSync(OUT, combined);

const sizeKB = Math.round(Buffer.byteLength(combined, 'utf8') / 1024);
console.log(`\nCombined worker written: ${OUT} (${sizeKB}KB)`);
if (sizeKB > 900) console.warn('WARNING: Worker size near 1MB limit!');
